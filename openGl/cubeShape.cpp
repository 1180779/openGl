cubeShape::cubeShape(glm::vec3 color) 
    : m_color(color)
{
    ++s_count;
    if (s_count > 1)
        return;
    glGenVertexArrays(1, &s_VAO);
    glBindVertexArray(s_VAO);

    glGenBuffers(1, &s_VBO);
    glBindBuffer(GL_ARRAY_BUFFER, s_VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(s_vertices), s_vertices, GL_STATIC_DRAW);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);

    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3*sizeof(float)));
    glEnableVertexAttribArray(1);
}

cubeShape::cubeShape(const cubeShape& other) 
    : objectShape(other), m_color(other.m_color)
{
    ++s_count;
}

cubeShape::~cubeShape()
{
    --s_count;
    if (s_count > 0)
        return;
    glDeleteVertexArrays(1, &s_VAO);
    glDeleteBuffers(1, &s_VBO);
}

void cubeShape::render(shader& sh) const
{
    objectShape::render(sh);
    sh.set3f(objectShape::s_colorShaderName, m_color.x, m_color.y, m_color.z);
    glBindVertexArray(s_VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
}
